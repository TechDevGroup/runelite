package net.runelite.client.plugins.runeutils;

import lombok.Data;

/**
 * Represents a filter profile for inventory item highlighting
 */
@Data
public class ItemProfile
{
	private String name = "New Profile";
	private String filter = "";
	private boolean enabled = true;
	private ValidationMode validationMode = ValidationMode.ANY;

	public enum ValidationMode
	{
		ALL("Match All"),
		ANY("Match Any"),
		NONE("Match None");

		private final String displayName;

		ValidationMode(String displayName)
		{
			this.displayName = displayName;
		}

		@Override
		public String toString()
		{
			return displayName;
		}
	}

	/**
	 * Check if an item name matches this profile's filter
	 */
	public boolean matches(String itemName)
	{
		if (!enabled || filter.isEmpty())
		{
			return false;
		}

		String[] filters = filter.split(",");
		int matchCount = 0;

		for (String f : filters)
		{
			String trimmed = f.trim().toLowerCase();
			if (!trimmed.isEmpty() && itemName.toLowerCase().contains(trimmed))
			{
				matchCount++;
			}
		}

		switch (validationMode)
		{
			case ALL:
				return matchCount == filters.length;
			case ANY:
				return matchCount > 0;
			case NONE:
				return matchCount == 0;
			default:
				return false;
		}
	}
}
